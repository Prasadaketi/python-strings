# Implicit string datatype

name = "prasad"
print(name)
print(type(name))

#explicit string datatype

message = str("Hello")
print(message)
print(type(message))

#variable/ datatype annotation

message : str = "Hi, How are you?"
print(message)
print(type(message))

#binary
a = str(0b0110)
print(a)
print(type(a))

#octal
a = str(0o3214)
print(a)
print(type(a))

#hexa
a = str(0x34bd)
print(a)
print(type(a))

#boolean
a = str(False)
print(a)
print(type(a))

#binary conversion
b = bin("prasad")
print(a)
print(type(a))

#octal conversion
b = oct("hello")
print(a)
print(type(a))

#hexa conversion
c = hex("hi")
print(a)
print(type(a))

#boolean conversion
d = bool("abc")
print(a)
